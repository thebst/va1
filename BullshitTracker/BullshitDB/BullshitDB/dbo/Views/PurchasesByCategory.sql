CREATE VIEW dbo.PurchasesByCategory
AS
SELECT dbo.Transactions.Id as TransactionId, dbo.Transactions.Description, dbo.Vendors.Category, ROUND(dbo.Transactions.Total - ExeptionTransactionTotal.Amount, 2) AS Amount
FROM     dbo.Transactions INNER JOIN
                      (SELECT TransactionId, SUM(AmountPreTax * 1.13) AS Amount
                       FROM      dbo.ExceptionTransactions AS ExceptionTransactions_1
                       GROUP BY TransactionId) AS ExeptionTransactionTotal ON dbo.Transactions.Id = ExeptionTransactionTotal.TransactionId INNER JOIN
                  dbo.Vendors ON dbo.Transactions.Vendor = dbo.Vendors.Id

union 
select TransactionId, Description, Category, AmountPreTax * 1.13 as Amount
from ExceptionTransactions


GO
